const _idLetters = ['A', 'B', 'C', 'D', 'E', 'F', 'G', 'H'];
const _idList = (() => {
  let ids = [],
    idCount = 100,
    idLength = 10;

  for (let i = 0; i < idCount; i++) {
    let str = '';
    for (let j = 0; j < idLength; j++) {
      str += _idLetters[Math.floor(Math.random() * _idLetters.length)];
    }
    ids.push(str);
  }

  return ids;
})();

const nouns = [..._nouns],
  verbs = [..._verbs],
  pageElIds = [..._idList];

function getRandomEl(arr: string[], randomNo: number): string {
  return arr[Math.floor(randomNo * arr.length)]!;
}

const getNoun = (randomNo?: number) => getRandomEl(nouns, randomNo ?? Math.random());
const getVerb = (randomNo?: number) => getRandomEl(verbs, randomNo ?? Math.random());
const getPageElId = (randomNo?: number) => getRandomEl(pageElIds, randomNo ?? Math.random());

export { getNoun, getVerb, getPageElId };
